SELECT
	id_pel AS "ID Pelicula",
    titulo_pel AS "Titulo",
    desc_gen AS "Genero",
    nombreApellido_act AS "Actor"
FROM 
	generos
    INNER JOIN peliculas USING (cod_gen)
    INNER JOIN repartos USING (id_pel)
    INNER JOIN actores USING (id_act)
ORDER BY 
	titulo_pel;

#Consigna 4b
